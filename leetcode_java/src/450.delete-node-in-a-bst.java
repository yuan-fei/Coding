/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public TreeNode deleteNode(TreeNode root, int key) {
        if(root == null){
        	return null;
        }
        if(root.val > key){
        	root.left = deleteNode(root.left, key);
        	return root;
        }
        if(root.val < key){
        	root.right = deleteNode(root.right, key);
        	return root;
        }
        
    	if(root.right == null){
    		return root.left;
    	}
    	else{
    		int v = findLeftmost(root.right);
    		root.val = v;
    		root.right = deleteNode(root.right, v);
    		return root;
    	}
        
    }

    int findLeftmost(TreeNode root){
    	while(root.left != null){
    		root = root.left;
    	}
    	return root.val;
    }
}
